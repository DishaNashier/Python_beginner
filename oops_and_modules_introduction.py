class Dog():
  def __init__(self,breed,age):
    self.breed=breed
    self.age=age
    self.tricks=[]
  def intro(self):
    print(f'dog breed is {self.breed} and dog age is {self.age}')
  def teach_trick(self,trick):
    self.tricks.append(trick)

dog1=Dog("libra",4)
